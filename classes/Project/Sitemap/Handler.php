<?php

namespace Project\Sitemap;

use eZ\Publish\Sitemap\Handler as Core;

abstract class Handler extends Core
{

    private static $oInstance = null;

    protected function __construct($sMode = 'global')
    {
        self::$oInstance = \eZINI::instance('ezsitemap.ini');

        parent::__construct($sMode);
    }


    /***********************************************************************************************************************/
    /***********************************************************************************************************************/
    /******************************************** Sitemaps "Global" methods ************************************************/
    /***********************************************************************************************************************/
    /***********************************************************************************************************************/


    final protected function getCustomEZObjectsUrl() {
        return self::$oInstance->variable('Urls', 'urlMainObjects');
    }


    /***********************************************************************************************************************/
    /***********************************************************************************************************************/
    /******************************************** Sitemaps "News" methods **************************************************/
    /***********************************************************************************************************************/
    /***********************************************************************************************************************/


    final protected function getGoogleNewsPublicationName() {
        return self::$oInstance->variable('GoogleNews', 'sPublicationName');
    }

}
